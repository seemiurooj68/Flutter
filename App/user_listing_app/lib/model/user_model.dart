class User {
  final String name;
  final String status;
  final String email;
  final String phone;
  final String bio;
  final String address;
  final String profilePicture;

  User({
    required this.name,
    required this.status,
    required this.email,
    required this.phone,
    required this.bio,
    required this.address,
    required this.profilePicture,
  });
}
